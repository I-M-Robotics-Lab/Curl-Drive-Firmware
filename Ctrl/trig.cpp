float sin(uint16_t theta16) noexcept {
    uint16_t t = theta16 & 0x3FFFu;
    uint16_t q = t >> 12;
    uint16_t i = t & 0x0FFFu;
    uint16_t idx = (q & 1u) ? static_cast<uint16_t>(0x0FFFu - i) : i;
    float v = SIN_Q1[idx];
    if (q >= 2u) v = -v;
    return v;
}

